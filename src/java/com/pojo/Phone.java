package com.pojo;

/**
 *
 * @author LENOVO
 */
public class Phone {
    
    private String id;
    private String msisdn;
    private String focalizado;
    private String status;
    private String dateRegistered;
    private String beginDate;
    private String endDate;
    private String marcacion;


    public String getId() {
        return id;
    }

  
    public void setId(String id) {
        this.id = id;
    }

   
    public String getMsisdn() {
        return msisdn;
    }

   
    public void setMsisdn(String msisdn) {
        this.msisdn = msisdn;
    }

    
    public String getFocalizado() {
        return focalizado;
    }

  
    public void setFocalizado(String focalizado) {
        this.focalizado = focalizado;
    }

    
    public String getStatus() {
        return status;
    }

   
    public void setStatus(String status) {
        this.status = status;
    }

   
    public String getDateRegistered() {
        return dateRegistered;
    }


    public void setDateRegistered(String dateRegistered) {
        this.dateRegistered = dateRegistered;
    }


    public String getBeginDate() {
        return beginDate;
    }

    
    public void setBeginDate(String beginDate) {
        this.beginDate = beginDate;
    }

    
    public String getEndDate() {
        return endDate;
    }

    
    public void setEndDate(String end_date) {
        this.endDate = end_date;
    }

   
    public String getMarcacion() {
        return marcacion;
    }

   
    public void setMarcacion(String marcacion) {
        this.marcacion = marcacion;
    }
     
    
    
}
