package com.pojo;


public class Response {

    private int code;
    private String responseResume;
    private String description;
    
  
    
    public int getCode() {
        return code;
    }

   
    public void setCode(int code) {
        this.code = code;
    }

 
    public String getResponseResume() {
        return responseResume;
    }

    
    public void setResponseResume(String responseResume) {
        this.responseResume = responseResume;
    }

    
    public String getDescription() {
        return description;
    }

   
    public void setDescription(String description) {
        this.description = description;
    }
    
   
    
    
}
